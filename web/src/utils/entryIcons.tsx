export function getEntryIcon(entryType?: string, toolName?: string, content?: string) {
  // Check for MCP tools
  if (toolName?.startsWith('mcp__')) {
    return McpIcon;
  }

  // Check content for thinking markers
  if (content?.includes('[Thinking]')) {
    return ThinkingIcon;
  }

  // Check content for tool markers
  if (content?.includes('[Tool:')) {
    return ToolUseIcon;
  }

  // Check entry type
  switch (entryType) {
    case 'user':
      return UserIcon;
    case 'assistant':
      return AssistantIcon;
    case 'tool_result':
      return ToolResultIcon;
    default:
      return DefaultIcon;
  }
}

export function getEntryTypeLabel(entryType?: string, hookEventName?: string, toolName?: string, content?: string): string {
  if (hookEventName) return hookEventName;

  // Check for MCP tools
  if (toolName?.startsWith('mcp__')) {
    return 'MCP';
  }

  // Check content for thinking markers
  if (content?.includes('[Thinking]')) {
    return 'Thinking';
  }

  // Check content for tool markers
  if (content?.includes('[Tool:')) {
    return 'Tool Use';
  }

  switch (entryType) {
    case 'user':
      return 'User';
    case 'assistant':
      return 'Response';
    case 'tool_result':
      return 'Tool Result';
    default:
      return entryType || 'Event';
  }
}
